package PP;

public class Surat {
    String idsurat;
    String namaMahasiswa;
    String kelas;
    char jenisKelamin;
    int usia;

    public Surat (String idsurat, String namaMahasiswa, String kelas, char jenisKelamin, int usia){

        this.idsurat = idsurat;
        this.namaMahasiswa = namaMahasiswa;
        this.kelas = kelas;
        this.jenisKelamin = jenisKelamin;
        this.usia = usia;
    }

}
